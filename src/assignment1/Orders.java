package assignment1;


public class Orders {

	private Items book;
	private float discount;
	private int num;
	private float total;
	
	public Items getBook() {
		return book;
	}
	public void setBook(Items book) {
		this.book = book;
	}
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	public float getTotal() {
		return total;
	}
	public void setTotal(float total) {
		this.total = total;
	}
	
	public float getDiscount() {
		return discount;
	}
	public void setDiscount(float discount) {
		this.discount = discount;
	}
	
	
}
